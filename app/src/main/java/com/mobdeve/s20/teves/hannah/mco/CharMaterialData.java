package com.mobdeve.s20.teves.hannah.mco;

public class CharMaterialData {
    private String name;
    private int amount;

    public CharMaterialData(String name, int amount) {
        this.name = name;
        this.amount = amount;
    }

    public String getName() {
        return name;
    }

    public int getAmount() {
        return amount;
    }
}
